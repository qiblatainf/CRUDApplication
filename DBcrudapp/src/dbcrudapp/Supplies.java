    public void UpdateTable() throws Exception{
        Class.forName("oracle.jdbc.driver.OracleDriver");
        conn = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:orcl", "system", "26188");
        if(conn == null){
            System.out.println("Unable to connect to database");
        }
        
        pst = conn.prepareStatement("SELECT * from SUPPLIES");
        ResultSet rs = pst.executeQuery();
        
        ResultSetMetaData rsd = rs.getMetaData();
        int c = rsd.getColumnCount();
        DefaultTableModel dft = (DefaultTableModel)jTable1.getModel();
        dft.setRowCount(0);
        
        while(rs.next()){
            Vector v2 = new Vector();
            for (int i = 1; i <= c; i++) {
                v2.add(rs.getInt("supnr"));
                v2.add(rs.getString("prodnr"));
                v2.add(rs.getString("purchase_price"));
                v2.add(rs.getString("deliv_period"));
       
            }
            
            dft.addRow(v2); 
            
        }
    }
